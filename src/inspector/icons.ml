let string_code ?(line_length = 80) filename =
  let icon = GdkPixbuf.from_file filename in
  let s = Gpointer.string_of_region (GdkPixbuf.get_pixels icon) in

  let isprint c = c >= '\032' && c <= '\126' in
  let escaped = function
  | '\\' | '\n' | '\r' | '\t' | '\b' as c -> Char.escaped c
  | '\"' -> "\\\""
  | c when isprint c -> Printf.sprintf "%c" c
  | c -> "\\" ^ Printf.sprintf "%03d" (int_of_char c) in
  let explode s =
    let rec exp i l =
      if i < 0 then l else exp (i - 1) (s.[i] :: l) in
    exp (String.length s - 1) [] in

  let rec shrink = function
  | _ :: _ :: _ :: '\000' :: tl -> '.' :: '.' :: '.' :: '\000' :: shrink tl
  | a :: b :: c :: d :: tl -> a :: b :: c :: d :: shrink tl
  | l -> l in

  String.concat "\\\n" (List.rev (List.fold_left
    (fun lines_list c ->
      match lines_list with
      | [] -> assert false
      | h :: tl ->
          let e = escaped c in
          let h' = h ^ e in
          if h = "" || String.length h' <= (line_length - 1)
            then h' :: tl else e :: h :: tl)
    [""] (shrink (explode s))))

